package com.OuSoft.system.entity;

import java.util.List;

public class TreeMenu {
    private Menu menu;
    private List<Menu> menu2;

    public Menu getMenu() {
        return menu;
    }

    public void setMenu(Menu menu) {
        this.menu = menu;
    }

    public List<Menu> getMenu2() {
        return menu2;
    }

    public void setMenu2(List<Menu> menu2) {
        this.menu2 = menu2;
    }

    @Override
    public String toString() {
        return "TreeMenu{" +
                "menu=" + menu +
                ", menu2=" + menu2 +
                '}';
    }
}

package com.OuSoft.system.entity;

/**
 * 薪酬规则
 */
public class CompensationRules {

    private Tsuser tsuser;

    public Tsuser getTsuser() {
        return tsuser;
    }

    public void setTsuser(Tsuser tsuser) {
        this.tsuser = tsuser;
    }

    private String id;//id
    private String rank;//级别
    private String minimum;//最低标准
    private String thehighest;//最高标准
    private String meritpay;//业绩工资
    private String userId;//操作人id
    private String inputTime;//录入时间
    private String describe;//备注

    public String getDescribe() {
        return describe;
    }

    public void setDescribe(String describe) {
        this.describe = describe;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getMinimum() {
        return minimum;
    }

    public void setMinimum(String minimum) {
        this.minimum = minimum;
    }

    public String getThehighest() {
        return thehighest;
    }

    public void setThehighest(String thehighest) {
        this.thehighest = thehighest;
    }

    public String getMeritpay() {
        return meritpay;
    }

    public void setMeritpay(String meritpay) {
        this.meritpay = meritpay;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getInputTime() {
        return inputTime;
    }

    public void setInputTime(String inputTime) {
        this.inputTime = inputTime;
    }
}

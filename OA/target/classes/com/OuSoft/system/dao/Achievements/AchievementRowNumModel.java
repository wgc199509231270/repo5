package com.OuSoft.system.dao.Achievements;

import java.util.List;

public class AchievementRowNumModel {
	private List<AchievementRowNum> list;
	private int Firstplace; // 第一名
	private int Lastplace;  // 最后一名
	private int Myplace;    // 我的名次
	private float FirstGap; // 与第一名的业绩差距
	private float LastGap;  // 与最后一名的业绩差距
	private float BehindGap;// 与后一名的业绩差距
	private float FrontGap; // 与前一名的业绩差距
	
	
	public float getBehindGap() {
		return BehindGap;
	}
	public void setBehindGap(float behindGap) {
		BehindGap = behindGap;
	}
	public float getFrontGap() {
		return FrontGap;
	}
	public void setFrontGap(float frontGap) {
		FrontGap = frontGap;
	}
	public List<AchievementRowNum> getList() {
		return list;
	}
	public void setList(List<AchievementRowNum> list) {
		this.list = list;
	}
	public int getFirstplace() {
		return Firstplace;
	}
	public void setFirstplace(int firstplace) {
		Firstplace = firstplace;
	}
	public int getLastplace() {
		return Lastplace;
	}
	public void setLastplace(int lastplace) {
		Lastplace = lastplace;
	}
	public int getMyplace() {
		return Myplace;
	}
	public void setMyplace(int myplace) {
		Myplace = myplace;
	}

	public float getFirstGap() {
		return FirstGap;
	}
	public void setFirstGap(float firstGap) {
		FirstGap = firstGap;
	}
	public float getLastGap() {
		return LastGap;
	}
	public void setLastGap(float lastGap) {
		LastGap = lastGap;
	}
}
